export default class AcademicMethod {
    constructor(id, specialization, academicDiscipline, activityType, academicMethodActivityType, academicMethodActivityForm, deadLine) {
        this.id = id;
        this.specialization = specialization;
        this.academicDiscipline = academicDiscipline;
        this.activityType = activityType;
        this.academicMethodActivityType = academicMethodActivityType;
        this.academicMethodActivityForm = academicMethodActivityForm;
        this.deadLine = deadLine;
    }
}